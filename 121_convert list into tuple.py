# converting list into tuple
  # By using tuply() function we can convert a list into tuple. But we cant change the list after turning it into tuple,because it becomes immutable.
print('--ex--')
days=['sun','mon','tues','wed','thu','fri','sat']
t=tuple(days)
print(t)


